def suma_numeros(*numeros):
    """Función que suma una lista de números."""
    return sum(numeros)

def multiplica_numeros(a, b):
    """Función que multiplica dos números."""
    return a * b

# Ejemplos de uso:
print(suma_numeros(1, 2, 3, 4, 5))  # Salida: 15
print(multiplica_numeros(2, 3))     # Salida: 6
